package ivn.typh.main;

public class LoginData {
	private String user;
	private String password;
	
	public LoginData(String u,String p){
		this.user=u;
		this.password = p;
	}
	
	public String getUser() {
		return user;
	}
	public String getPassword() {
		return password;
	}
	
	
	
}
